    private void loadDataKhachHangToTbl() {
        String searchText = txtTimKiem.getText().toLowerCase();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Mã người dùng", "Tên người dùng", "Số điện thoại", "Email"});
        if (searchText.isEmpty()) {
            listNguoiDung = nguoiDungService.selectAllByIdVaiTro(3);

        } else {
            String sql = "SELECT * FROM NGUOIDUNG"
                    + " WHERE ( TENNGUOIDUNG LIKE N'%" + searchText + "%' "
                    + "OR SODIENTHOAI LIKE N'%" + searchText + "%' "
                    + "OR MANGUOIDUNG LIKE N'%" + searchText + "%' "
                    + "OR SODIENTHOAI LIKE N'%" + searchText + "%' "
                    + "OR EMAIL LIKE N'%" + searchText + "%' ) "
                    + "AND MAVAITRO LIKE = 3";
            System.out.println(sql);
            listNguoiDung = nguoiDungService.selectAllByCustomSql(sql);
        }
        for (NguoiDung nguoiDung : listNguoiDung) {
            model.addRow(new Object[]{nguoiDung.getMaNguoiDung(), nguoiDung.getTenNguoiDung(), nguoiDung.getSoDienThoai(), nguoiDung.getEmail()});
        }
        tblKhachHang.setModel(model);
    }

